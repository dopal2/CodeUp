import java.util.*;
public class q1403 {
    public static void main(String[] arg) throws Exception{
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int[] arr = new int[n];
        for(int i=0; i<n; i++) arr[i] = sc.nextInt();
        for(int num:arr) System.out.println(num);
        for(int num:arr) System.out.println(num);
    }
}
